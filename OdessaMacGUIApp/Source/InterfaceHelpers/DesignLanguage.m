#import "DesignLanguage.h"

@implementation DesignLanguage

+ (NSColor*) lightGrayColor
{ // 204,204,204
    return [NSColor colorWithCalibratedRed:0.8 green:0.8 blue:0.8 alpha:1];
}

+ (NSColor*) midGrayColor
{ // 153,153,153
    return [NSColor colorWithCalibratedRed:0.6 green:0.6 blue:0.6 alpha:1];
}

@end
